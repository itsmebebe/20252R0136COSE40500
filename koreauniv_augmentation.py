class RGBShift:
    """Randomly shift RGB channels in an image."""
    def __init__(self, r_shift=0.1, g_shift=0.1, b_shift=0.1):
        self.r_shift = r_shift
        self.g_shift = g_shift
        self.b_shift = b_shift

    def __call__(self, img):
        # Apply random shift to each channel
        r_shift = (torch.rand(1) - 0.5) * 2 * self.r_shift
        g_shift = (torch.rand(1) - 0.5) * 2 * self.g_shift
        b_shift = (torch.rand(1) - 0.5) * 2 * self.b_shift
        img[0] = torch.clamp(img[0] + r_shift, 0, 1)
        img[1] = torch.clamp(img[1] + g_shift, 0, 1)
        img[2] = torch.clamp(img[2] + b_shift, 0, 1)
        return img

# Apply RGB shift and visualize
rgb_shift = RGBShift()
shifted_images = torch.stack([rgb_shift(img) for img in images])
imshow(torchvision.utils.make_grid(shifted_images))

